     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $table = DB::table('historicos')
        ->join('livros', 'livros.id', '=', 'historicos.id_livro')
        ->join('locacaos', 'locacaos.id', '=', 'historicos.id_locacao')
        ->select('locacaos.*', 'livros.*', 'locacaos.id as codigo')
        ->where('locacaos.status', '=', 'Fechado')
        ->where('locacaos.id_cliente', '=', $id)
        ->get();

        return $table;
    }

